export function t(
  language: Language,
  key: string,
  vars: Record<string, string | number> = {},
): string {
  const entry = dictionary[language][key] ?? dictionary.en[key];

  if (!entry) {
    return key;
  }

  return typeof entry === "function" ? entry(vars) : entry;
}
